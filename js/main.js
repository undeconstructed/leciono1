let localTranslations = {}

function flag(code) {
  if (code === 'eo') {
    // let f = document.createElement('div')
    // f.classList.add('eoflag')
    return 'Eo'
  }
  if (code === 'en') {
    code = 'gb'
  } else if (code === 'pt') {
    code = 'br'
  }

  const aL = 'a'.codePointAt(0)
  const aF = '🇦'.codePointAt(0)

  let out = ''
  for (let l of code) {
    let c = l.codePointAt(0)
    let o = aF + (c - aL)
    out += String.fromCodePoint(o)
  }

  return out
}

function setupLanguageChooser() {
  let chooser = document.querySelector('.languagechooser')
  let list = chooser.querySelector('.list')

  let set = code => {
    chooser.setAttribute('code', code)

    document.dispatchEvent(new CustomEvent('language-change', { detail: code }))
  }

  let isOpen = false

  let close = (selected) => {
    isOpen = false
    chooser.classList.remove('open')
    document.removeEventListener('click', close)
    if (selected) {
      selected.scrollIntoView({ block: 'center' })
    }
  }

  let click = e => {
    if (!isOpen) {
      isOpen = true
      chooser.classList.add('open')
      document.addEventListener('click', clickOut)
    } else {
      let t = e.target
      let l = t.getAttribute('code')
      if (l) {
        close(t)
        set(l)
      }
    }
  }

  let clickOut = e => {
    if (!chooser.contains(e.target)) {
      close()
    }
  }

  let addLanguage = code => {
    let opt = document.createElement('div')
    opt.setAttribute("code", code)

    if (code == 'eo') {
      opt.classList.add('eoflag')
    } else {
      opt.append(flag(code))
    }

    list.append(opt)
  }

  let setup = () => {
    let langs = [...new Set([...Object.keys(translations), ...Object.keys(localTranslations)])].sort()
    for (let lang of langs) {
      addLanguage(lang)
    }
  }
  setup()

  let firstLang = navigator.language.split('-')[0]
  if (translations[firstLang]) {
    set(firstLang)
  } else {
    set('en')
  }

  chooser.addEventListener('click', click)

  document.addEventListener('new-language', e => {
    setup()
    set(e.detail)
    dispatch()
  })
}

function setupMainPage() {
  let translate = lang => {
    let baseText = translations['en']
    let text = translations[lang] || {}
    let localText = localTranslations[lang] || {}

    for (let e of document.querySelectorAll('.learn')) {
      let id = e.getAttribute('tid')
      let line = localText[id]
      if (line) {
        line = '* ' + line
      }
      e.innerHTML = line || text[id] || baseText[id]
      // e.lang = lang
    }

    document.title = text['title'] || baseText['title']
    document.documentElement.lang = lang
  }

  let setupAudioElement = e => {
    let id = e.getAttribute('fid')
    let eAudio = document.createElement('audio')
    eAudio.src = `sound/e${id}.mp3`
    e.append(eAudio)
    e.addEventListener('click', _e => {
      eAudio.play()
    })
    eAudio.addEventListener('play', _e => {
      e.classList.add('playing')
    })
    eAudio.addEventListener('ended', _e => {
      e.classList.remove('playing')
    })
    e.append('🔉\ufe0e')
  }

  let setupAudio = () => {
    for (let e of document.querySelectorAll('.listen')) {
      setupAudioElement(e)
    }
  }

  document.addEventListener('language-change', e => {
    translate(e.detail)
  })

  setupAudio()

  let setupFlow = () => {
    let page = document.querySelector('.lessonpage')
    let nextBox = page.querySelector('.next')

    nextBox.replaceChildren()

    for (let e of document.querySelectorAll('.part')) {
      e.style.opacity = 0
    }

    let onscroll= e => {
      let d = document.documentElement.scrollTop + document.documentElement.clientHeight - 50
      for (let e of document.querySelectorAll('.part')) {
        if (d > e.offsetTop) {
          e.style.opacity = 1
        }
      }
    }

    onscroll()

    document.addEventListener('scroll', onscroll)
  }

  setupFlow()
}

function setupTranslationPage() {
  let tmpl = document.getElementById('translationtmpl').content.firstElementChild

  let lc = localStorage.getItem('translations')
  if (lc) {
    localTranslations = JSON.parse(lc)
  }

  let nowLanguage = null
  let translationInputs = []
  let asJsonBox = document.querySelector('.asjson')

  let translate = lang => {
    let text = translations[lang] || {}
    let localText = localTranslations[lang] || {}

    for (let {id, box} of translationInputs) {
      box.value = localText[id] || text[id] || ''
      box.lang = lang
    }

    showJson(lang)
  }

  let showJson = l => {
    let trans0 = translations[nowLanguage] || {}
    let t = `"${l}": {\n`
    for (let {id, box} of translationInputs) {
      if (box.value) {
        t += `  "${id}": ${JSON.stringify(box.value)},\n`
      } else if (trans0[id]) {
        t += `  "${id}": ${JSON.stringify(trans0[id])},\n`
      }
    }
    t += '}'
    asJsonBox.textContent = t
  }

  let saveTranslation = () => {
    let trans0 = translations[nowLanguage] || {}
    let texts = {}
    for (let {id, box} of translationInputs) {
      if (box.value && box.value != trans0[id]) {
        texts[id] = box.value
      }
    }
    localTranslations[nowLanguage] = texts

    localStorage.setItem('translations', JSON.stringify(localTranslations))

    showJson(nowLanguage)
  }

  // for (let e of document.querySelectorAll('.learn')) {
  //   let id = e.getAttribute('tid')
  //   let idtag = document.createElement('span')
  //   idtag.classList.add('idtag')
  //   idtag.textContent = `[#${id}]`
  //   e.prepend(idtag)
  // }

  let setup2 = () => {
    let eoText = translations['eo']

    let translationsBox = document.querySelector('.translations')
    for (let e of document.querySelectorAll('.learn')) {
      let id = e.getAttribute('tid')
      let tb = tmpl.cloneNode(true)
      tb.querySelector('.src').textContent = eoText[id]
      let box = tb.querySelector('input[type=text]')
      box.addEventListener('change', e => {
        saveTranslation(nowLanguage)
      })

      translationsBox.append(tb)

      translationInputs.push({
        id: id,
        box: box,
      })
    }

    document.querySelector('button[name=addlanguage]').addEventListener('click', e => {
      e.preventDefault()
      let lang = prompt('Nova lingvo (du- aŭ tri- litera kodo):')
      if (lang && !translations[lang]) {
        translations[lang] = {}

        document.dispatchEvent(new CustomEvent('new-language', { detail: lang }))
      }
    })

    document.querySelector('button[name=cleartranslation]').addEventListener('click', e => {
      e.preventDefault()
      if (confirm('Ĉu forigi lokan tradukon por la nuna lingvo?')) {
        delete localTranslations[nowLanguage]
        // XXX - copy and pasted to save
        localStorage.setItem('translations', JSON.stringify(localTranslations))
        // XXX - copy pasted to trigger refresh
        document.dispatchEvent(new CustomEvent('language-change', { detail: nowLanguage }))
      }
    })
  }

  let toLesson = () => {
    document.body.classList.remove('trans')
    // XXX - copy pasted to trigger refresh
    document.dispatchEvent(new CustomEvent('language-change', { detail: nowLanguage }))
  }

  let firstTime = true
  let toTrans = () => {
    if (firstTime) {
      setup2()
      firstTime = false
    }
    translate(nowLanguage)
    document.body.classList.add('trans')
  }

  let onTrans = false
  document.body.addEventListener('dblclick', e => {
    if (e.ctrlKey) {
      onTrans = !onTrans
      if (onTrans) {
        toTrans()
      } else {
        toLesson()
      }
    }
  })

  document.addEventListener('language-change', e => {
    nowLanguage = e.detail
    if (onTrans) {
      translate(nowLanguage)
    }
  })
}

